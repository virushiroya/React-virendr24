// Determine the grade based on a score

let marks = 80

if (marks >= 75) 
{
    console.log("Grade A");
}
else if (marks >= 60 && marks < 75) 
{
    console.log("Grade B");
}
else if (marks >= 45 && marks < 60)
{
    console.log("Grade C");
}
else if (marks >= 35 && marks < 45) 
{
    console.log("Grade D");
}
else if (marks < 35) 
{
    console.log("Fail.");
}
